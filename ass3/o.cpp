#include<bits/stdc++.h>

using namespace std;

int main(){
   char n;
   int a,b;
   cin >> a >> n >> b;

   if(n == '+'){
    std::cout << a + b << endl;
   }if(n == '-'){
    std::cout << a - b << endl;
   }if(n == '*'){
    std::cout << a * b << endl;
   }if(n == '/'){
    std::cout << a / b << endl;
   }

    
}


